// Bodybuilding utility functions
export const getBodybuildingWorkoutById = (id, split) => {
  if (bodybuildingWorkouts[split]) {
    return bodybuildingWorkouts[split].find((workout) => workout.id === id);
  }
  return null;
};

export const getWorkoutsByType = (split, type) => {
  if (bodybuildingWorkouts[split]) {
    return bodybuildingWorkouts[split].filter(
      (workout) => workout.type === type
    );
  }
  return [];
};

export const getBodybuildingWorkoutPlan = (split, daysPerWeek) => {
  const workouts = bodybuildingWorkouts[split] || [];
  const plan = [];

  if (split === "PPL") {
    const push = workouts.filter((w) => w.type === "Push");
    const pull = workouts.filter((w) => w.type === "Pull");
    const legs = workouts.filter((w) => w.type === "Legs");

    if (daysPerWeek === 3) {
      plan.push({ day: "Day 1: Push", exercises: push.slice(0, 5) });
      plan.push({ day: "Day 2: Pull", exercises: pull.slice(0, 5) });
      plan.push({ day: "Day 3: Legs", exercises: legs.slice(0, 5) });
    } else if (daysPerWeek === 6) {
      plan.push({ day: "Day 1: Push", exercises: push.slice(0, 6) });
      plan.push({ day: "Day 2: Pull", exercises: pull.slice(0, 6) });
      plan.push({ day: "Day 3: Legs", exercises: legs.slice(0, 6) });
      plan.push({ day: "Day 4: Push", exercises: push.slice(6, 12) });
      plan.push({ day: "Day 5: Pull", exercises: pull.slice(6, 12) });
      plan.push({ day: "Day 6: Legs", exercises: legs.slice(6, 12) });
    }
  } else if (split === "UpperLower") {
    const upper = workouts.filter((w) => w.type === "Upper");
    const lower = workouts.filter((w) => w.type === "Lower");

    if (daysPerWeek === 2) {
      plan.push({ day: "Day 1: Upper", exercises: upper.slice(0, 6) });
      plan.push({ day: "Day 2: Lower", exercises: lower.slice(0, 6) });
    } else if (daysPerWeek === 4) {
      plan.push({ day: "Day 1: Upper", exercises: upper.slice(0, 6) });
      plan.push({ day: "Day 2: Lower", exercises: lower.slice(0, 6) });
      plan.push({ day: "Day 3: Upper", exercises: upper.slice(6, 12) });
      plan.push({ day: "Day 4: Lower", exercises: lower.slice(6, 12) });
    }
  } else if (split === "BroSplits") {
    const chest = workouts.filter((w) => w.type === "Chest");
    const back = workouts.filter((w) => w.type === "Back");
    const shoulders = workouts.filter((w) => w.type === "Shoulders");
    const arms = workouts.filter((w) => w.type === "Arms");
    const legs = workouts.filter((w) => w.type === "Legs");
    const abs = workouts.filter((w) => w.type === "Abs");

    if (daysPerWeek === 5) {
      plan.push({ day: "Day 1: Chest", exercises: chest.slice(0, 5) });
      plan.push({ day: "Day 2: Back", exercises: back.slice(0, 5) });
      plan.push({ day: "Day 3: Shoulders", exercises: shoulders.slice(0, 4) });
      plan.push({ day: "Day 4: Arms", exercises: arms.slice(0, 5) });
      plan.push({ day: "Day 5: Legs", exercises: legs.slice(0, 6) });
    } else if (daysPerWeek === 6) {
      plan.push({ day: "Day 1: Chest", exercises: chest.slice(0, 5) });
      plan.push({ day: "Day 2: Back", exercises: back.slice(0, 5) });
      plan.push({ day: "Day 3: Shoulders", exercises: shoulders.slice(0, 4) });
      plan.push({ day: "Day 4: Arms", exercises: arms.slice(0, 5) });
      plan.push({ day: "Day 5: Legs", exercises: legs.slice(0, 6) });
      plan.push({ day: "Day 6: Abs/Cardio", exercises: abs.slice(0, 3) });
    }
  } else if (split === "ArnoldSplits") {
    const chestBack = workouts.filter(
      (w) => w.type === "Chest" || w.type === "Back"
    );
    const shouldersArms = workouts.filter(
      (w) => w.type === "Shoulders" || w.type === "Arms"
    );
    const legs = workouts.filter((w) => w.type === "Legs");
    const abs = workouts.filter((w) => w.type === "Abs");

    if (daysPerWeek === 4) {
      plan.push({
        day: "Day 1: Chest & Back",
        exercises: chestBack.slice(0, 6),
      });
      plan.push({
        day: "Day 2: Shoulders & Arms",
        exercises: shouldersArms.slice(0, 6),
      });
      plan.push({ day: "Day 3: Legs", exercises: legs.slice(0, 6) });
      plan.push({
        day: "Day 4: Chest & Back",
        exercises: chestBack.slice(6, 12),
      });
    } else if (daysPerWeek === 6) {
      plan.push({
        day: "Day 1: Chest & Back",
        exercises: chestBack.slice(0, 6),
      });
      plan.push({
        day: "Day 2: Shoulders & Arms",
        exercises: shouldersArms.slice(0, 6),
      });
      plan.push({ day: "Day 3: Legs", exercises: legs.slice(0, 6) });
      plan.push({
        day: "Day 4: Chest & Back",
        exercises: chestBack.slice(6, 12),
      });
      plan.push({
        day: "Day 5: Shoulders & Arms",
        exercises: shouldersArms.slice(6, 12),
      });
      plan.push({
        day: "Day 6: Legs & Abs",
        exercises: [...legs.slice(6, 9), ...abs.slice(0, 2)],
      });
    }
  }

  return plan;
};

export default {
  bodybuildingWorkouts,
  getBodybuildingWorkoutById,
  getWorkoutsByType,
  getBodybuildingWorkoutPlan,
};
