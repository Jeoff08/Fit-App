// Medical conditions utility functions
export const getMedicalWorkoutsByCondition = (condition) => {
  return medicalConditionWorkouts[condition] || []
}

export const getMedicalWorkoutById = (id) => {
  for (const condition in medicalConditionWorkouts) {
    const workout = medicalConditionWorkouts[condition].find((w) => w.id === id)
    if (workout) return workout
  }
  return null
}

export const getMedicalWorkoutPlan = (condition, intensity = "low") => {
  const workouts = medicalConditionWorkouts[condition] || []
  const filteredWorkouts = workouts.filter((w) => w.intensity === intensity)

  return {
    condition: condition,
    intensity: intensity,
    exercises: filteredWorkouts.slice(0, 6),
    duration: "4-6 weeks",
    frequency: "3-4 times per week",
    notes: "Always consult with healthcare provider before starting any medical exercise program",
  }
}

export default {
  medicalConditionWorkouts,
  getMedicalWorkoutsByCondition,
  getMedicalWorkoutById,
  getMedicalWorkoutPlan,
}